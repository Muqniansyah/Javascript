// Mengosongkan Array

let colors = ["red", "green", "blue"];

console.log("Sebelum mengosongkan:", colors); // ['red', 'green', 'blue']

// Mengosongkan array
colors.length = 0;

console.log("Setelah mengosongkan:", colors); // []

// Pada contoh di atas, kita mengatur length array menjadi 0 untuk mengosongkan array.

// Manipulasi panjang array seperti ini dapat berguna tergantung pada kebutuhan Anda, tetapi perlu diingat bahwa mengubah panjang array juga dapat memengaruhi elemen-elemen di dalamnya, jadi gunakan dengan hati-hati.
