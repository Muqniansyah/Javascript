// Menghapus Elemen dari Array

let numbers = [1, 2, 3, 4, 5];

console.log("Sebelum menghapus:", numbers); // [1, 2, 3, 4, 5]

// Mengurangi panjang array
numbers.length = 3;

console.log("Setelah menghapus:", numbers); // [1, 2, 3]

// Pada contoh ini, kita mengurangi panjang array numbers menjadi 3, sehingga elemen yang berada setelah indeks 2 akan dihapus.
