// filter digunakan untuk menyaring elemen berdasarkan kondisi tertentu.

// Membuat array angka
const angka = [1, 2, 3, 4, 5];

// Menggunakan filter untuk menyaring angka yang lebih besar dari 2
const hasilFilter = angka.filter((nilai) => nilai > 2);

console.log(hasilFilter); // Output: [3, 4, 5]

// kita menggunakan filter untuk menyaring angka yang lebih besar dari 2 dan mengembalikan array yang hanya berisi angka-angka tersebut.
