// map digunakan untuk mentransformasi setiap elemen dalam array.

// Membuat array angka
const angka = [1, 2, 3, 4, 5];

// Menggunakan map untuk menggandakan setiap angka
const hasilMap = angka.map((nilai) => nilai * 2);

console.log(hasilMap); // Output: [2, 4, 6, 8, 10]

// kita menggunakan map untuk menggandakan setiap angka dalam array.
