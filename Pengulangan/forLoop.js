// struktur dasar dari for
// for (inisialisasi variabel, tes kondisi, perubahan nilai variabel)
// atau bisa juga diartikan seperti :
// for(mulainya perulangan, berhenti perulangan, proses decrement/increment)

// contoh dari for loop
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// contoh lain dari for loop
const myArray = ["Muni", "Rangga", "Rahman", "kelvin", "Revand"];
for (let a = 0; a < myArray.length; a++) {
  console.log(myArray[a]);
}
