// setInterval(): Menjadwalkan eksekusi fungsi secara berkala.

// Fungsi yang akan dieksekusi setiap 2000 milidetik (2 detik)
function tampilkanPesan() {
  console.log("Pesan ini muncul setiap 2 detik.");
}

// Mengeksekusi fungsi setiap 2000 milidetik (2 detik)
setInterval(tampilkanPesan, 2000);

// Dalam contoh ini, fungsi tampilkanPesan akan mencetak pesan ke konsol setiap 2 detik.
