// operator perbandingan

let nilai;
nilai = 10;

if (nilai == 10) {
  console.log("Nilai sama dengan 10");
} else if (nilai != 10) {
  console.log("Nilai tidak sama dengan 10");
} else if (nilai === 12) {
  console.log("Nilai Strict sama dengan/identik 12");
} else if (nilai !== 10) {
  console.log("Nilai Strict tidak sama dengan/tidak identik 10");
} else if (nilai > 45) {
  console.log("Nilai lebih besar dari 45");
} else if (nilai < 40) {
  console.log("Nilai lebih kecil dari 40");
} else if (nilai >= 55) {
  console.log("Nilai lebih besar sama dengan 55");
} else if (nilai <= 60) {
  console.log("Nilai lebih kecil sama dengan 60");
}
