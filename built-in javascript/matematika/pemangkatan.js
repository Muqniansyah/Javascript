// Pemangkatan (Exponentiation):

function pangkat(angka, pangkat) {
  return Math.pow(angka, pangkat);
}

const hasilPangkat = pangkat(2, 3);
console.log("Hasil Pemangkatan:", hasilPangkat);

// Fungsi Math.pow() digunakan untuk menghitung hasil pangkat dari suatu angka. Dalam sintaks umumnya, Math.pow(x, y) mengembalikan x pangkat y.
